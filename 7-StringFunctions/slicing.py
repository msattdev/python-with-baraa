# Index and Slicing
date = '2024-09-15'

# Extract the year
print(date[0:4]) # 4 is up to and not including
print(date[:4]) # You dont have to specify the zero if you are starting from the beginning

# Extract the month
print(date[5:7])

# Extract the day
print(date[-2:]) # Use negative numbers up to and including the end
